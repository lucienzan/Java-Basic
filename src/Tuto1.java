public class Tuto1 {
    public static void main(String[] args)
    {
        // printf
        char myChar = '@';
        boolean isShow = true;
        String name = "Lucien";
        int intNumber = 12;

        // b for boolean, c for char, d for integer
        System.out.printf("%b this is printf",isShow+ "\n");
        System.out.printf("%s this is printf",name+"\n");
        System.out.printf("%d this is printf",intNumber);
        System.out.printf("%c this is printf",myChar);

    }
}
